                 We are part of a network of actors working to build alternative and more equitable economic models. We purchase coffee in a private market where value and pricing can be negotiated directly between farmers and buyers. Together, we work with farming communities, local mill owners and exporters to fix prices that are higher and more stable year to year than the alternative commercial floor price which fluctuates dramatically based on global supply and demand, or weather patterns.
                 
